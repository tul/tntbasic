// Sticks the chars in the handle
long CStdOutHandle::WriteCharsToConsole(
	char					*inBuffer,
	long					inSize)
{
	if (mHandle)
	{
		Size				curr=::GetHandleSize(mHandle);
		
		ThrowIfOSErr_(::BetterSetHandleSize(mHandle,curr+inSize,0));
		::BlockMove(inBuffer,*mHandle+curr,inSize);
	}
	else
	{
		mHandle=::NewHandle(inSize);
		ThrowIfMemFull_(mHandle);
		
		::BlockMove(inBuffer,*mHandle,inSize);
	}
	
	return inSize;
}
