// A class to encrypt and decrypt TNT Basic Basc resources. This code is not exactly unbreakable...
// A simple XOR is used with a keyword. It's weak, way weak, but it puts off casual snoopers.

#include "UEncryption.h"

// ÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑ
//		¥ SimpleEncryptDecrypt						Static
// ÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑ
// Does a simple XOR encryption on the given handle using the given keyword (repeating the keyword
// when it gets to the end).
void UEncryption::SimpleEncryptDecrypt(
	Handle			ioHandle,
	const char		*inKeyword)
{
	if (!ioHandle)
		return;

	const char		*data=inKeyword;
	Size			handleSize=::GetHandleSize(ioHandle);
	
	for (Size index=0; index<handleSize; index++)
	{
		// xor with char from keyword string
		(*ioHandle)[index]^=*data;

		// wrap when a null char is hit
		data++;
		if (!*data)
			data=inKeyword;
	}
}